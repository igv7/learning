
public class Zadacha1 {
	
	//бесконечный цикл

	public static void main(String[] args) {
		
		int start = Integer.MAX_VALUE - 1;
		for (int i = start; i <= start + 1; i++) {
            //бесконечный цикл
            System.out.println(i); //убеждаемся в бесконечности цикла
        }

	}

}
